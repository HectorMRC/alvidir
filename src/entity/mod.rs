use crate::id::{Id, Identified};
use crate::name::Name;
use serde::{Deserialize, Serialize};

/// An Entity is anything which to interact with.
#[derive(Clone, Serialize, Deserialize)]
pub struct Entity {
    #[serde(flatten)]
    id: Id<Entity>,
    #[serde(flatten)]
    name: Name<Entity>,
}

impl Eq for Entity {}
impl PartialEq for Entity {
    fn eq(&self, other: &Self) -> bool {
        self.id == other.id
    }
}

impl Identified<Entity> for Entity {
    fn id(&self) -> Id<Entity> {
        self.id
    }
}

impl Entity {
    /// Creates a new entity with the given name and an autogenerated id.
    pub fn new(name: Name<Entity>) -> Self {
        Self {
            id: Id::new(),
            name,
        }
    }

    /// Creates a new entity with the given id and name.
    pub fn with_id(id: Id<Entity>, name: Name<Entity>) -> Self {
        Self { id, name }
    }

    /// Returns a reference to the name of self.
    pub fn name(&self) -> &Name<Entity> {
        &self.name
    }
}
